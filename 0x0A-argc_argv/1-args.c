#include <stdio.h>
#include <stdlib.h>

/**
 * main - check the code
 *
 * Description : a program that print its own name.
 *
 * @argc: The number of command line arguments
 * @argv: An array of command line arguments
 *
 * Return: Always 0.
 */
int main(int argc, char __attribute__((unused)) *argv[])
{
	int i, count;


	for (i = 0; i < argc; i++)
	{
		count = i;

	}
	printf("%d\n", count);
	return (0);
}
